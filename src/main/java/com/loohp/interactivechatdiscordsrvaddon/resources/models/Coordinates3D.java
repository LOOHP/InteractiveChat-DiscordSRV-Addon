package com.loohp.interactivechatdiscordsrvaddon.resources.models;

public class Coordinates3D {

    private double x;
    private double y;
    private double z;

    public Coordinates3D(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getZ() {
        return z;
    }

}
